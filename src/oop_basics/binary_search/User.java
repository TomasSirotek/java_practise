package oop_basics.binary_search;

public class User {

    // attributes
    int id;
    String name;

    // constructor
    public User(int id, String name)
    {
        super();
        this.id = id;
        this.name = name;
    }

    // returns student id
    public int getId() { return id; }

    // returns student name
    public String getName() { return name; }

}
